// qwenSmallDense returns a BKC config for a compact Qwen3 dense checkpoint.
// Every small Qwen3 model shares the same vLLM launch shape, so we synthesise
// the Config to cut down on repetition.
func qwenSmallDense(id, name, modelID string, minVRAMGB float64) Config {
	return Config{
		ID:       id,
		Name:     name,
		Workload: WorkloadVLLM,
		ModelID:  modelID,
		Image:    imageVLLMStable,
		Port:     "8000",
		ExtraArgs: strings.Join([]string{
			"--trust-remote-code",
			"--tensor-parallel-size 1",
			"--max-model-len 32768",
			"--reasoning-parser qwen3",
		}, " "),
		Volumes:         hfMountDefault,
		Runtime:         runtimeDefault,
		Description:     name + " — single-GPU default.",
		Source:          "vllm-project/recipes Qwen/Qwen3.md",
		TargetDevices:   []string{DeviceRTX4090, DeviceRTX5090, DeviceL40S, DeviceA100_80, DeviceH100_80, DeviceH200, DeviceB200, DeviceRTXPRO6000, DeviceGB10},
		MinVRAMGBPerGPU: minVRAMGB,
		MinGPUCount:     1,
		Quantization:    QuantBF16,
		Arch:            ArchHopper,
	}
}
